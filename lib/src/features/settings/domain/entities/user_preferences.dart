/// Theme modes available in the application.
enum AppThemeMode {
  /// Uses the system's light or dark mode setting.
  system,

  /// Forces light mode.
  light,

  /// Forces dark mode.
  dark,
}

/// Unit systems for weight measurements.
enum WeightUnit {
  /// Kilograms.
  kg,

  /// Pounds.
  lb,
}

/// Unit systems for height measurements.
enum HeightUnit {
  /// Centimeters.
  cm,

  /// Feet and inches.
  ftIn,
}
